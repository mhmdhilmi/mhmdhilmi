				<h2>Real Madrid CF</h2>
				Real Madrid Club de Fútbol, yang biasa disebut Real Madrid, adalah klub sepak bola yang berbasis di Madrid. Klub ini berkompetisi di La Liga, kasta tertinggi sepak bola Spanyol. Didirikan pada tahun 1902 sebagai Madrid Football Club, klub ini secara tradisional mengenakan seragam kandang berwarna putih. Gelar kehormatan 'Real' dalam bahasa Spanyol berarti "Kerajaan" dan diberikan oleh Alfonso XIII pada tahun 1920. Real Madrid telah memainkan pertandingan kandang mereka di Santiago Bernabéu yang berkapasitas 78.297 penonton sejak tahun 1947. Tidak seperti kebanyakan klub olahraga Eropa, anggota Real Madrid telah memiliki dan mengoperasikan klub sepanjang sejarahnya. Klub ini adalah salah satu klub yang paling banyak didukung di dunia dan klub olahraga yang paling banyak diikuti di media sosial. Real Madrid adalah salah satu klub sepak bola paling sukses di dunia dan klub paling berprestasi di Eropa. Dalam sepak bola domestik, klub ini telah memenangkan 71 trofi; rekor 36 gelar La Liga, 20 Copa del Rey, 13 Supercopa de España, satu Copa Eva Duarte dan satu Copa de la Liga. Dalam sepak bola internasional Real Madrid telah memenangkan rekor 35 trofi: rekor 15 gelar Piala Eropa/Liga Champions UEFA, rekor enam Piala Super UEFA, dua Piala UEFA, rekor bersama dua Piala Latin, rekor satu Piala Iberoamerika, dan rekor sembilan kejuaraan dunia. Madrid telah menduduki peringkat pertama bersama dalam jumlah rekor di Peringkat Klub Dunia IFFHS. Di UEFA, Madrid menduduki peringkat pertama dalam peringkat klub sepanjang masa. <div id="more"><a href="Prak32.php">Baca selengkapnya</a></div>
				
