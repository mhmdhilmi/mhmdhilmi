				<h2>Atlético Madrid</h2>
				Club Atlético de Madrid, umumnya disebut sebagai Atlético Madrid atau hanya Atlético dan secara informal sebagai Atleti, adalah klub sepak bola profesional Spanyol yang berbasis di Madrid yang bermain di La Liga. Klub ini memainkan pertandingan kandangnya di Riyadh Air Metropolitano, yang memiliki kapasitas 70.692. Didirikan pada 26 April 1903 sebagai Athletic Club Sucursal de Madrid (cabang dari Athletic Club yang berbasis di Bilbao ), entitas ini bergabung dengan Club Aviación Nacional pada tahun 1939, dan menjadi Atlético de Madrid pada tahun 1946. Setelah tahun 1987, Jesús Gil mengawasi transformasi dari klub olahraga menjadi sociedad anónima deportiva (SAD), yang memperkuat kendali keluarganya atas struktur kepemilikan saham melalui penyelewengan. Seragam tradisional klub terdiri dari kemeja bergaris vertikal merah dan putih serta celana pendek biru. Para pendukungnya dijuluki sebagai colchoneros ('pembuat kasur'), ​​indios ('orang Indian'), dan rojiblancos ('merah putih'). Pangeran Felipe, yang kemudian menjadi Raja Felipe VI, telah menjadi presiden kehormatan klub sejak tahun 2003. Atlético adalah salah satu klub Spanyol tersukses, telah memenangkan 11 gelar La Liga, termasuk gelar ganda liga dan piala pada tahun 1996. Trofi domestik lainnya termasuk 10 gelar Copa del Rey, dua Supercopa de España, satu Copa Presidente FEF, dan satu Copa Eva Duarte. Mereka juga telah memenangkan banyak gelar di Eropa, termasuk Piala Winners Eropa pada tahun 1962, Liga Europa UEFA pada tahun 2010, 2012, dan 2018, dan Piala Super UEFA pada tahun 2010, 2012, dan 2018, selain Piala Interkontinental 1974. Di Liga Champions UEFA, Atlético mencapai final pada tahun 1974, 2014, dan 2016. Mereka memiliki persaingan yang kuat dengan tetangga mereka di Madrid, Real Madrid, yang selalu menjadi lawan mereka dalam derbi Madrid. <div id="more"><a href="Prak33.php">Baca selengkapnya</a></div>
				
